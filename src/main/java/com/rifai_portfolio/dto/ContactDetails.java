package com.rifai_portfolio.dto;

public class ContactDetails {
    private String mobileNum;
    private String email;
    private String yahoo;

    public String getMobileNum() { return mobileNum; }
    public void setMobileNum(String mobileNum) { this.mobileNum = mobileNum; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getYahoo() { return yahoo; }
    public void setYahoo(String yahoo) { this.yahoo = yahoo; }
}
